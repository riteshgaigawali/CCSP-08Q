/*
- In a variable we can store a single value, but when we need to store multiple values inside a variable we go for an Array.
- Whenever we store the values in an Array, they will be stored in an indexed format and index will start from 0.
*/
class Array1{
	public static void main(String args[]){
		int[] arr = {10, 20, 30, 40, 50};
		System.out.println(arr[0]);
		System.out.println(arr[1]);
		System.out.println(arr[2]);
		System.out.println(arr[3]);
		System.out.println(arr[4]);
	}
}