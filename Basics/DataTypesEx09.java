/*
 - The result of operations performed on two operands with different datatypes gives the result as follows.
 */
class DataTypesEx09 {
	public static void main(String args[]) {

		System.out.println(10 + 20); // int + int = int
		System.out.println(10.5 + 20.5); // float + float = float
		System.out.println(10 + 20.5); // int + float = float

	}

}