/*
- What us difference between length() and length ?
>> length() is primarily used to calculate the length of String whereas length is used to calculate the length or size of an array.
*/
class Array3{
	public static void main(String args[]){
		
		String str = "CyberSuccess";
		System.out.println(str.length());
		
		int[] arr = {10, 20, 30, 40, 50};
		System.out.println(arr.length);
		
	}
}