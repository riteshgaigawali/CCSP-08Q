/*
- A Character can be represented as pair of single quotes(' ').
- Within the pair of a single quotes we can represent a character in the form of letter, special character or numbers.
*/
class DataTypesEx11 {

	public static void main(String args[]) {
		char a1 = 'x';
		System.out.println(a1);
		char a2 = '3';
		System.out.println(a2);
		char a3 = '#';
		System.out.println(a3);

	}

}